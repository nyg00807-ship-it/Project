// 페이지네이션 설정
const itemsPerPage = 5; // 페이지당 항목 수
let currentPage = 1; // 현재 페이지
const totalPages = Math.ceil(faqData.length / itemsPerPage); // 총 페이지 수
let isFirstLoad = true; // ✅ 추가: 첫 로드 여부 확인

// FAQ 항목 렌더링 함수
function renderFAQItems(page) {
  const container = document.getElementById('FAQAccodion');
  container.innerHTML = ''; // 기존 항목 제거
  
  // 현재 페이지에 표시할 항목의 시작/끝 인덱스 계산
  const startIndex = (page - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const itemsToShow = faqData.slice(startIndex, endIndex);
  
  // FAQ 항목 생성
  itemsToShow.forEach((item, index) => {
    const faqItem = document.createElement('div');
    faqItem.className = 'FAQ-accordion-item';
    if (index === 0) faqItem.classList.add('active'); // 첫 번째만 열림
    
    faqItem.innerHTML = `
      <div class="FAQ-accordion-header">
        <div class="FAQ-accordion-Hbox">
          <span>Q</span>
          <div class="FAQ-accordion-title">
            <h4>${item.category}</h4>
            <p>${item.question}</p>
          </div>
        </div>
        <img src="icon/Top_Arrow(big).svg" alt="FAQ아코디언 화살표">
      </div>
      <div class="FAQ-accordion-content">
        <p>${item.answer}</p>
      </div>
    `;
    
    container.appendChild(faqItem);
  });
  
  // 아코디언 클릭 이벤트 추가
  addAccordionEvents();
}

// 아코디언 토글 기능
function addAccordionEvents() {
  const headers = document.querySelectorAll('.FAQ-accordion-header');
  
  headers.forEach(header => {
    header.addEventListener('click', () => {
      const item = header.parentElement;
      const isActive = item.classList.contains('active');
      
      // 모든 항목 닫기
      document.querySelectorAll('.FAQ-accordion-item').forEach(i => {
        i.classList.remove('active');
      });
      
      // 클릭한 항목만 열기/닫기
      if (!isActive) {
        item.classList.add('active');
      }
    });
  });
}

// 페이지 번호 렌더링 함수
function renderPageNumbers() {
  const numbersContainer = document.getElementById('numbers');
  numbersContainer.innerHTML = '';
  
  const isMobile = window.innerWidth <= 768;
  const maxVisiblePages = isMobile ? 5 : 10;

  const currentGroup = Math.floor((currentPage - 1) / maxVisiblePages);
  const startPage = currentGroup * maxVisiblePages + 1;
  const endPage = Math.min(startPage + maxVisiblePages - 1, totalPages);

  for (let i = startPage; i <= endPage; i++) {
    const li = document.createElement('li');
    if (i === currentPage) li.classList.add('active');
    
    li.innerHTML = `<a href="#">${i}</a>`;
    
    // 페이지 번호 클릭 이벤트
    li.addEventListener('click', (e) => {
      e.preventDefault();
      goToPage(i, true); // ✅ 수정: true 추가 (스크롤 함)
    });
    
    numbersContainer.appendChild(li);
  }
}

// 특정 페이지로 이동
function goToPage(page, shouldScroll = true) { // ✅ 수정: 매개변수 추가
  if (page < 1 || page > totalPages) return;
  
  currentPage = page;
  renderFAQItems(currentPage);
  renderPageNumbers();
  
  // ✅ 수정: 조건부 스크롤
  if (shouldScroll && !isFirstLoad) {
    const isMobile = window.innerWidth <= 768;
    window.scrollTo({ 
      top: isMobile ? 300 : 400, 
      behavior: 'smooth' 
    });
  }
}

// 페이지네이션 버튼 이벤트
function initPaginationButtons() {
  // 첫 페이지로
  document.querySelector('.arrow-double-left').addEventListener('click', () => {
    goToPage(1, true); // ✅ 수정: true 추가
  });
  
  // 이전 페이지
  document.querySelector('.arrow-left').addEventListener('click', () => {
    goToPage(currentPage - 1, true); // ✅ 수정: true 추가
  });
  
  // 다음 페이지
  document.querySelector('.arrow-right').addEventListener('click', () => {
    goToPage(currentPage + 1, true); // ✅ 수정: true 추가
  });
  
  // 마지막 페이지로
  document.querySelector('.arrow-double-right').addEventListener('click', () => {
    goToPage(totalPages, true); // ✅ 수정: true 추가
  });
}

// 초기화
function init() {
  renderFAQItems(currentPage);
  renderPageNumbers();
  initPaginationButtons();
  
  isFirstLoad = false; // ✅ 추가: 첫 로드 완료 표시
  
  // 화면 크기 변경 시 페이지네이션 재렌더링
  let resizeTimer;
  window.addEventListener('resize', function() {
    clearTimeout(resizeTimer);
    resizeTimer = setTimeout(function() {
      renderPageNumbers();
    }, 250);
  });
}

// 페이지 로드 시 실행
document.addEventListener('DOMContentLoaded', init);